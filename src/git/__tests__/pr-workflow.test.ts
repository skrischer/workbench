// src/git/__tests__/pr-workflow.test.ts

import { describe, it, expect, vi, beforeEach, afterEach } from 'vitest';
import { execSync } from 'node:child_process';
import { createPr, getPrStatus, listOpenPrs, type DodResult } from '../pr-workflow.js';

// Mock execSync
vi.mock('node:child_process', () => ({
  execSync: vi.fn(),
}));

// Mock diff-summary module
vi.mock('../diff-summary.js', () => ({
  generateSummary: vi.fn(() => ({
    stats: {
      filesChanged: 2,
      linesAdded: 42,
      linesRemoved: 12,
      files: [
        { path: 'src/file1.ts', linesAdded: 30, linesRemoved: 5 },
        { path: 'src/file2.ts', linesAdded: 12, linesRemoved: 7 },
      ],
    },
    commits: ['abc123 Add feature', 'def456 Fix bug'],
    markdown: '## Changes (auto-generated by workbench)\n### Files Modified\n- `src/file1.ts` (+30/-5)\n- `src/file2.ts` (+12/-7)\n### Stats\n- Files changed: 2\n- Lines added: 42\n- Lines removed: 12\n### Commits\n- abc123 Add feature\n- def456 Fix bug\n',
  })),
}));

const mockedExecSync = vi.mocked(execSync);

describe('pr-workflow', () => {
  beforeEach(() => {
    vi.clearAllMocks();
  });

  afterEach(() => {
    vi.resetAllMocks();
  });

  it('should build correct gh pr create command', async () => {
    // Mock successful gh checks and PR creation
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) {
          return '/usr/bin/gh\n';
        }
        if (command.includes('gh auth status')) {
          return 'Logged in to github.com\n';
        }
        if (command.includes('gh pr create')) {
          // Verify command structure
          expect(command).toContain('--base "main"');
          expect(command).toContain('--head "feature-branch"');
          expect(command).toContain('--title "Add new feature"');
          expect(command).toContain('--body');
          return 'https://github.com/owner/repo/pull/42\n';
        }
      }
      return '';
    });

    const result = await createPr({
      branch: 'feature-branch',
      baseBranch: 'main',
      title: 'Add new feature',
    });

    expect(result.number).toBe(42);
    expect(result.url).toBe('https://github.com/owner/repo/pull/42');
    expect(result.state).toBe('open');
    expect(result.reviewStatus).toBe('pending');
  });

  it('should map PR review status correctly', async () => {
    // Test APPROVED status
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
        if (command.includes('gh pr view')) return 'APPROVED\n';
      }
      return '';
    });

    const approved = await getPrStatus(42);
    expect(approved).toBe('approved');

    vi.clearAllMocks();

    // Test CHANGES_REQUESTED status
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
        if (command.includes('gh pr view')) return 'CHANGES_REQUESTED\n';
      }
      return '';
    });

    const changesRequested = await getPrStatus(42);
    expect(changesRequested).toBe('changes_requested');

    vi.clearAllMocks();

    // Test pending status
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
        if (command.includes('gh pr view')) return 'REVIEW_REQUIRED\n';
      }
      return '';
    });

    const pending = await getPrStatus(42);
    expect(pending).toBe('pending');
  });

  it('should reject PR creation when DoD fails', async () => {
    const failedDod: DodResult = {
      passed: false,
      checks: [
        { name: 'TypeScript', passed: true },
        { name: 'Tests', passed: false, message: '3 tests failed' },
        { name: 'Linting', passed: false, message: 'ESLint errors found' },
      ],
      summary: 'DoD failed: 2 of 3 checks failed',
    };

    // Mock gh checks
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
      }
      return '';
    });

    await expect(
      createPr({
        branch: 'feature-branch',
        baseBranch: 'main',
        title: 'Add feature',
        dodResult: failedDod,
      })
    ).rejects.toThrow('Cannot create PR: DoD checks failed');
  });

  it('should throw error when gh is not installed', async () => {
    // Mock gh not found
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string' && command.includes('which gh')) {
        throw new Error('Command not found');
      }
      return '';
    });

    await expect(
      createPr({
        branch: 'feature-branch',
        baseBranch: 'main',
        title: 'Add feature',
      })
    ).rejects.toThrow('gh CLI is not installed');
  });

  it('should throw error when gh is not authenticated', async () => {
    // Mock gh installed but not authenticated
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) {
          return '/usr/bin/gh\n';
        }
        if (command.includes('gh auth status')) {
          throw new Error('Not authenticated');
        }
      }
      return '';
    });

    await expect(
      createPr({
        branch: 'feature-branch',
        baseBranch: 'main',
        title: 'Add feature',
      })
    ).rejects.toThrow('gh CLI is not authenticated');
  });

  it('should list open PRs correctly', async () => {
    // Mock PR list output
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
        if (command.includes('gh pr list')) {
          return JSON.stringify([
            {
              number: 1,
              title: 'First PR',
              url: 'https://github.com/owner/repo/pull/1',
              state: 'open',
              reviewDecision: 'APPROVED',
            },
            {
              number: 2,
              title: 'Second PR',
              url: 'https://github.com/owner/repo/pull/2',
              state: 'open',
              reviewDecision: null,
            },
            {
              number: 3,
              title: 'Third PR',
              url: 'https://github.com/owner/repo/pull/3',
              state: 'open',
              reviewDecision: 'CHANGES_REQUESTED',
            },
          ]);
        }
      }
      return '';
    });

    const prs = await listOpenPrs();

    expect(prs).toHaveLength(3);
    expect(prs[0].reviewStatus).toBe('approved');
    expect(prs[1].reviewStatus).toBe('pending');
    expect(prs[2].reviewStatus).toBe('changes_requested');
  });

  it('should create PR with all options including DoD and runId', async () => {
    const passedDod: DodResult = {
      passed: true,
      checks: [
        { name: 'TypeScript', passed: true },
        { name: 'Tests', passed: true },
        { name: 'Linting', passed: true },
      ],
      summary: 'All checks passed',
    };

    // Mock successful flow
    mockedExecSync.mockImplementation((command) => {
      if (typeof command === 'string') {
        if (command.includes('which gh')) return '/usr/bin/gh\n';
        if (command.includes('gh auth status')) return 'Logged in\n';
        if (command.includes('gh pr create')) {
          // Verify DoD is included in body
          expect(command).toContain('### DoD');
          expect(command).toContain('✅ All checks passed');
          return 'https://github.com/owner/repo/pull/123\n';
        }
      }
      return '';
    });

    const result = await createPr({
      branch: 'feature-branch',
      baseBranch: 'main',
      title: 'Complete feature with DoD',
      runId: 'run-456',
      dodResult: passedDod,
      cwd: '/test/path',
    });

    expect(result.number).toBe(123);
    expect(result.url).toBe('https://github.com/owner/repo/pull/123');
  });
});
